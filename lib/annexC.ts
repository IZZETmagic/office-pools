// Build the lookup map: key = sorted qualifying groups joined by comma
// Each row's qualifying groups = the unique set of letters in its assignments
const ANNEX_C_MAP = new Map<string, { option: number; assignment: AnnexCAssignment }>()

for (const [option, a, b, d, e, g, i, k, l] of RAW) {
  const groups = [a, b, d, e, g, i, k, l].sort()
  const key = groups.join(',')
  ANNEX_C_MAP.set(key, {
    option,
    assignment: { '1A': a, '1B': b, '1D': d, '1E': e, '1G': g, '1I': i, '1K': k, '1L': l },
  })
}

/**
 * Look up the Annex C assignment for a given set of 8 qualifying groups.
 * @param qualifyingGroups - The 8 group letters whose third-place teams qualified
 * @returns The assignment and option number, or null if not found
 */
export function lookupAnnexC(
  qualifyingGroups: string[]
): { option: number; assignment: AnnexCAssignment } | null {
  if (qualifyingGroups.length !== 8) return null
  const key = [...qualifyingGroups].sort().join(',')
  return ANNEX_C_MAP.get(key) ?? null
}
